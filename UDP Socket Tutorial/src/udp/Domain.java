public class Domain {
	
	private String address = "";
	private String host = "";
	
	public Domain (String addr, String hst) {
		addr = address;
		hst = host;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getHost() {
		return host;
	}

	public void setHost(String host) {
		this.host = host;
	}
	
}
